function [y,gr,he] = fundAnonymous10D(w)
%--------------------------------------------------------------------------
% INFORMACION fundAnonymous2D(w)
% Realiza un switch-case de todas las funciones
%----------------------------------INPUT-----------------------------------
% w         Tipo de funcion         (Valor)
%----------------------------------OUTPUT----------------------------------
% y         Valor de la Funcion     (Vector 1 x p)
% gr        Gradiente Simbolico     (Function Handle)
% he        Hessiana                (Function Handle)
%-----------------------------Tipos de Funciones---------------------------
% - Brown
% - Pathological
% - Streched V Sine Wave
% - Wavy:
%--------------------------------------------------------------------------
syms x1 x2 x3 x4 x5 x6 x7 x8 x9 x10
switch w
    case 1
        y = (x1^2)^((x2^2)+1)+(x2^2)^((x1^2)+1) + (x2^2)^((x3^2)+1)+(x3^2)^((x2^2)+1) + (x3^2)^((x4^2)+1)+(x4^2)^((x3^2)+1) + (x4^2)^((x5^2)+1)+(x5^2)^((x4^2)+1) + (x5^2)^((x6^2)+1)+(x6^2)^((x5^2)+1) + (x6^2)^((x7^2)+1)+(x7^2)^((x6^2)+1) + (x7^2)^((x8^2)+1)+(x8^2)^((x7^2)+1) + (x8^2)^((x9^2)+1)+(x9^2)^((x8^2)+1) + (x9^2)^((x10^2)+1)+(x10^2)^((x9^2)+1);
    case 2
        y = 0.5 +((sin(sqrt(100*(x1^2)+(x2^2)))^2)-0.5)/(1+0.001*((x1^2)-2*x1*x2+(x2^2))^2) + 0.5 +((sin(sqrt(100*(x2^2)+(x3^2)))^2)-0.5)/(1+0.001*((x2^2)-2*x2*x3+(x3^2))^2) + 0.5 +((sin(sqrt(100*(x3^2)+(x4^2)))^2)-0.5)/(1+0.001*((x3^2)-2*x3*x4+(x4^2))^2) + 0.5 +((sin(sqrt(100*(x4^2)+(x5^2)))^2)-0.5)/(1+0.001*((x4^2)-2*x4*x5+(x5^2))^2) + 0.5 +((sin(sqrt(100*(x5^2)+(x6^2)))^2)-0.5)/(1+0.001*((x5^2)-2*x5*x6+(x6^2))^2) + 0.5 +((sin(sqrt(100*(x6^2)+(x7^2)))^2)-0.5)/(1+0.001*((x6^2)-2*x6*x7+(x7^2))^2) + 0.5 +((sin(sqrt(100*(x7^2)+(x8^2)))^2)-0.5)/(1+0.001*((x7^2)-2*x7*x8+(x8^2))^2) + 0.5 +((sin(sqrt(100*(x8^2)+(x9^2)))^2)-0.5)/(1+0.001*((x8^2)-2*x8*x9+(x9^2))^2) + 0.5 +((sin(sqrt(100*(x9^2)+(x10^2)))^2)-0.5)/(1+0.001*((x9^2)-2*x9*x10+(x10^2))^2);
    case 3
        y = power((x2^2 + x1^2),0.25)*((sin(50*(x2^2 + x1^2)^0.1)^2)+0.1) + power((x3^2 + x2^2),0.25)*((sin(50*(x3^2 + x2^2)^0.1)^2)+0.1) + power((x4^2 + x3^2),0.25)*((sin(50*(x4^2 + x3^2)^0.1)^2)+0.1) + power((x5^2 + x4^2),0.25)*((sin(50*(x5^2 + x4^2)^0.1)^2)+0.1) + power((x6^2 + x5^2),0.25)*((sin(50*(x6^2 + x5^2)^0.1)^2)+0.1) + power((x7^2 + x6^2),0.25)*((sin(50*(x7^2 + x6^2)^0.1)^2)+0.1) + power((x8^2 + x7^2),0.25)*((sin(50*(x8^2 + x7^2)^0.1)^2)+0.1) + power((x9^2 + x8^2),0.25)*((sin(50*(x9^2 + x8^2)^0.1)^2)+0.1) + power((x10^2 + x9^2),0.25)*((sin(50*(x10^2 + x9^2)^0.1)^2)+0.1);
    case 4
        y = 1-(1/10)*cos(10*x1)*exp(-((x1)^2)/2) + 1-(1/10)*cos(10*x2)*exp(-((x2)^2)/2) + 1-(1/10)*cos(10*x3)*exp(-((x3)^2)/2) + 1-(1/10)*cos(10*x4)*exp(-((x4)^2)/2) + 1-(1/10)*cos(10*x5)*exp(-((x5)^2)/2) + 1-(1/10)*cos(10*x6)*exp(-((x6)^2)/2) + 1-(1/10)*cos(10*x7)*exp(-((x7)^2)/2) + 1-(1/10)*cos(10*x8)*exp(-((x8)^2)/2) + 1-(1/10)*cos(10*x9)*exp(-((x9)^2)/2) + 1-(1/10)*cos(10*x10)*exp(-((x10)^2)/2);
    otherwise
        disp('No es un numero valido')
end

symbolicgradient=gradient(y,[x1, x2, x3, x4, x5, x6, x7, x8, x9, x10]);
gr=matlabFunction(symbolicgradient);
symbolichessian=hessian(y,[x1, x2, x3, x4, x5, x6, x7, x8, x9, x10]);
he=matlabFunction(symbolichessian);
y=matlabFunction(y);
end % End de la funcion